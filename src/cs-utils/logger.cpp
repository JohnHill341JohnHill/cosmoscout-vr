////////////////////////////////////////////////////////////////////////////////////////////////////

spdlog::sink_ptr getCoutSink() {
  static auto sink = std::make_shared<spdlog::sinks::stdout_color_sink_mt>();
  return sink;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

spdlog::sink_ptr getFileSink() {
  static auto sink = std::make_shared<spdlog::sinks::basic_file_sink_mt>("cosmoscout.log");
  return sink;
}

